<?php
namespace Gineign\GmoPayment\Input;

/**
 * <b>口振セレクト請求依頼　入力パラメータクラス</b>
 *
 * @package com.gmo_pg.client
 * @subpackage input
 * @see inputPackageInfo.php
 * @author GMO PaymentGateway
 */
class ExecTranBankaccountInput extends BaseInput
{

    /**
     * @var string 取引ID
     */
    private $accessID;
    /**
     * @var string 取引パスワード
     */
    private $accessPass;
    /**
     * @var string オーダーID
     */
    private $orderID;
    /**
     * @var string サイトID
     */
    private $siteID;
    /**
     * @var string サイトパスワード
     */
    private $sitePass;
    /**
     * @var string 会員ID
     */
    private $memberID;
    /**
     * @var string 振替指定日
     */
    private $targetDate;
    /**
     * @var string 請求内容
     */
    private $remarks;
    /**
     * @var string 加盟店自由項目1
     */
    private $clientField1;
    /**
     * @var string 加盟店自由項目2
     */
    private $clientField2;
    /**
     * @var string 加盟店自由項目3
     */
    private $clientField3;
    /**
     * @var string チェックモード
     */
    private $checkMode;


    /**
     * コンストラクタ
     *
     * @param array $params 入力パラメータ
     */
    public function __construct($params = null)
    {
        parent::__construct($params);
    }

    /**
     * デフォルト値設定
     */
    public function setDefaultValues()
    {

    }

    /**
     * 入力パラメータ群の値を設定する
     *
     * @param IgnoreCaseMap $params 入力パラメータ
     */
    public function setInputValues($params)
    {
        // 入力パラメータがnullの場合は設定処理を行わない
        if (is_null($params)) {
            return;
        }

        $this->setAccessID($this->getStringValue($params, 'AccessID', $this->getAccessID()));
        $this->setAccessPass($this->getStringValue($params, 'AccessPass', $this->getAccessPass()));
        $this->setOrderID($this->getStringValue($params, 'OrderID', $this->getOrderID()));
        $this->setSiteID($this->getStringValue($params, 'SiteID', $this->getSiteID()));
        $this->setSitePass($this->getStringValue($params, 'SitePass', $this->getSitePass()));
        $this->setMemberID($this->getStringValue($params, 'MemberID', $this->getMemberID()));
        $this->setTargetDate($this->getStringValue($params, 'TargetDate', $this->getTargetDate()));
        $this->setRemarks($this->getStringValue($params, 'Remarks', $this->getRemarks()));
        $this->setClientField1($this->getStringValue($params, 'ClientField1', $this->getClientField1()));
        $this->setClientField2($this->getStringValue($params, 'ClientField2', $this->getClientField2()));
        $this->setClientField3($this->getStringValue($params, 'ClientField3', $this->getClientField3()));
        $this->setCheckMode($this->getStringValue($params, 'CheckMode', $this->getCheckMode()));

    }

    /**
     * 取引ID取得
     * @return string 取引ID
     */
    public function getAccessID()
    {
        return $this->accessID;
    }

    /**
     * 取引ID設定
     *
     * @param string $accessID
     */
    public function setAccessID($accessID)
    {
        $this->accessID = $accessID;
    }

    /**
     * 取引パスワード取得
     * @return string 取引パスワード
     */
    public function getAccessPass()
    {
        return $this->accessPass;
    }

    /**
     * 取引パスワード設定
     *
     * @param string $accessPass
     */
    public function setAccessPass($accessPass)
    {
        $this->accessPass = $accessPass;
    }

    /**
     * オーダーID取得
     * @return string オーダーID
     */
    public function getOrderID()
    {
        return $this->orderID;
    }

    /**
     * オーダーID設定
     *
     * @param string $orderID
     */
    public function setOrderID($orderID)
    {
        $this->orderID = $orderID;
    }

    /**
     * サイトID取得
     * @return string サイトID
     */
    public function getSiteID()
    {
        return $this->siteID;
    }

    /**
     * サイトID設定
     *
     * @param string $siteID
     */
    public function setSiteID($siteID)
    {
        $this->siteID = $siteID;
    }

    /**
     * サイトパスワード取得
     * @return string サイトパスワード
     */
    public function getSitePass()
    {
        return $this->sitePass;
    }

    /**
     * サイトパスワード設定
     *
     * @param string $sitePass
     */
    public function setSitePass($sitePass)
    {
        $this->sitePass = $sitePass;
    }

    /**
     * 会員ID取得
     * @return string 会員ID
     */
    public function getMemberID()
    {
        return $this->memberID;
    }

    /**
     * 会員ID設定
     *
     * @param string $memberID
     */
    public function setMemberID($memberID)
    {
        $this->memberID = $memberID;
    }

    /**
     * 振替指定日取得
     * @return string 振替指定日
     */
    public function getTargetDate()
    {
        return $this->targetDate;
    }

    /**
     * 振替指定日設定
     *
     * @param string $targetDate
     */
    public function setTargetDate($targetDate)
    {
        $this->targetDate = $targetDate;
    }

    /**
     * 請求内容取得
     * @return string 請求内容
     */
    public function getRemarks()
    {
        return $this->remarks;
    }

    /**
     * 請求内容設定
     *
     * @param string $remarks
     */
    public function setRemarks($remarks)
    {
        $this->remarks = $remarks;
    }

    /**
     * 加盟店自由項目1取得
     * @return string 加盟店自由項目1
     */
    public function getClientField1()
    {
        return $this->clientField1;
    }

    /**
     * 加盟店自由項目1設定
     *
     * @param string $clientField1
     */
    public function setClientField1($clientField1)
    {
        $this->clientField1 = $clientField1;
    }

    /**
     * 加盟店自由項目2取得
     * @return string 加盟店自由項目2
     */
    public function getClientField2()
    {
        return $this->clientField2;
    }

    /**
     * 加盟店自由項目2設定
     *
     * @param string $clientField2
     */
    public function setClientField2($clientField2)
    {
        $this->clientField2 = $clientField2;
    }

    /**
     * 加盟店自由項目3取得
     * @return string 加盟店自由項目3
     */
    public function getClientField3()
    {
        return $this->clientField3;
    }

    /**
     * 加盟店自由項目3設定
     *
     * @param string $clientField3
     */
    public function setClientField3($clientField3)
    {
        $this->clientField3 = $clientField3;
    }

    /**
     * チェックモード取得
     * @return string チェックモード
     */
    public function getCheckMode()
    {
        return $this->checkMode;
    }

    /**
     * チェックモード設定
     *
     * @param string $checkMode
     */
    public function setCheckMode($checkMode)
    {
        $this->checkMode = $checkMode;
    }

    /**
     * 文字列表現
     * @return string 接続文字列表現
     */
    public function toString()
    {
        $str = '';
        $str .= 'AccessID=' . $this->encodeStr($this->getAccessID());
        $str .= '&';
        $str .= 'AccessPass=' . $this->encodeStr($this->getAccessPass());
        $str .= '&';
        $str .= 'OrderID=' . $this->encodeStr($this->getOrderID());
        $str .= '&';
        $str .= 'SiteID=' . $this->encodeStr($this->getSiteID());
        $str .= '&';
        $str .= 'SitePass=' . $this->encodeStr($this->getSitePass());
        $str .= '&';
        $str .= 'MemberID=' . $this->encodeStr($this->getMemberID());
        $str .= '&';
        $str .= 'TargetDate=' . $this->encodeStr($this->getTargetDate());
        $str .= '&';
        $str .= 'Remarks=' . $this->encodeStr($this->getRemarks());
        $str .= '&';
        $str .= 'ClientField1=' . $this->encodeStr($this->getClientField1());
        $str .= '&';
        $str .= 'ClientField2=' . $this->encodeStr($this->getClientField2());
        $str .= '&';
        $str .= 'ClientField3=' . $this->encodeStr($this->getClientField3());
        $str .= '&';
        $str .= 'CheckMode=' . $this->encodeStr($this->getCheckMode());

        return $str;
    }


}

